bp = False


def sendDataByte(byte):
    global bp
    if bp == True:
        return buspirate.sendDataByte(byte)
        
    return bgb_master.sendDataByte(byte)

def sendGb(data,wait = 0.05,resend = True):
    ret = b''
    for i in data:
        val = sendDataByte(i)
        time.sleep(wait)
        while val == 0xFE and resend == True:
            val = sendDataByte(i)
            time.sleep(wait)
        ret = ret + bytes((val,))
        
    print("Sent: " + str(binascii.hexlify(data)))
    print("Recv: " + str(binascii.hexlify(ret)))
    return ret
def sendUntil(data,waitfor,wait = 0.2):
    retval = None
