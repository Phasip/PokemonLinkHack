def getString(text,length):
    ret = b""
    if len(text) >= length:
        print("Text too long!")
        sys.exit(1)
    ret = pkmn_chars.string_to_gb(text)
    ret = ret + b"\x50"*(length-len(ret)) #String end and fill
    return ret


